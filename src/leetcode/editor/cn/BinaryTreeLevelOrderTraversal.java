public class TreeNode{
    int val;
    TreeNode left;
    TreeNode right;

    public TreeNode(int val) {
        this.val = val;
    }

    public TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
class Solution {
    Deque<TreeNode> queue = new LinkedList<>();
    List<List<Integer>> result = new ArrayList<>();

    public List<List<Integer>> levelOrder(TreeNode root) {
        if(root ==null){
            return result;
        }

        // æ ¹èŠ‚ç‚¹åŠ å…¥é˜Ÿåˆ—
        queue.add(root);

        // å½“é˜Ÿåˆ—ä¸ä¸ºç©ºçš„æ—¶å€™(æ³¨æ„ï¼šä¸èƒ½ç”¨nullåˆ¤æ–­)
        while(!queue.isEmpty()){
            // è®°å½•æ¯ä¸€å±‚çš„å…ƒç´ 
            List<Integer> path = new ArrayList<>();
            // éå†æ¯ä¸€å±‚çš„å…ƒç´ 
            int len = queue.size();
            for(int i = 0; i < len; i++){
                TreeNode node = queue.removeFirst();
                path.add(node.val);
                if(node.left != null){
                    queue.addLast(node.left);
                }
                if(node.right != null){
                    queue.addLast(node.right);
                }
            }

            result.add(new ArrayList<>(path));
        }

        return result;

    }
}
